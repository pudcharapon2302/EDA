class GooglePlacesClient:
    """
    Client สำหรับเรียกใช้ Google Places API (Place Details - Legacy)
    """

    def __init__(self, api_key: str):
        if not api_key:
            raise ValueError("Google API Key ต้องไม่เป็นค่าว่าง กรุณาตรวจสอบการตั้งค่า")
        self.api_key = api_key
        self.base_url = "https://maps.googleapis.com/maps/api/place/details/json"
        self.session = requests.Session()
        logger.info("GooglePlacesClient ถูกสร้างขึ้นแล้ว")

    def get_place_details(self, place_id: str, fields: str = None, language: str = 'th') -> Optional[Dict]:
        """
        ดึงข้อมูลรายละเอียดของสถานที่จาก place_id

        Args:
            place_id (str): Google Place ID
            fields (str): ฟิลด์ที่ต้องการ (คั่นด้วย comma). หากไม่ระบุ จะใช้ฟิลด์เริ่มต้น
            language (str): ภาษาที่ต้องการให้ผลลัพธ์แสดง (เช่น 'th' สำหรับภาษาไทย, 'en' สำหรับภาษาอังกฤษ)

        Returns:
            Dict: ข้อมูลสถานที่ หรือ None ถ้าเกิดข้อผิดพลาด
        """

        # ฟิลด์เริ่มต้นถ้าไม่ระบุ
        if fields is None:
            fields = (
                "name,formatted_address,geometry,formatted_phone_number,"
                "website,rating,user_ratings_total,price_level,opening_hours,"
                "types,business_status,photos,reviews"
            )

        params = {
            'place_id': place_id,
            'fields': fields,
            'key': self.api_key,
            'language': language
        }

        try:
            logger.debug(f"กำลังเรียก API สำหรับ Place ID: {place_id} ด้วยพารามิเตอร์: {params}")
            response = self.session.get(self.base_url, params=params)
            response.raise_for_status() # ตรวจสอบ HTTP errors (4xx, 5xx)

            data = response.json()

            if data['status'] == 'OK':
                logger.debug(f"API Response OK สำหรับ {place_id}")
                return data['result']
            elif data['status'] == 'ZERO_RESULTS':
                logger.warning(f"ไม่พบผลลัพธ์สำหรับ Place ID: {place_id} (Status: ZERO_RESULTS)")
                return None
            else:
                logger.error(f"API Error สำหรับ {place_id}: สถานะ '{data['status']}' ข้อความ: {data.get('error_message', 'ไม่มีข้อความผิดพลาด')}")
                return None

        except requests.exceptions.HTTPError as http_err:
            logger.error(f"HTTP Error สำหรับ {place_id}: {http_err} - Response: {response.text}")
            return None
        except requests.exceptions.ConnectionError as conn_err:
            logger.error(f"Connection Error สำหรับ {place_id}: {conn_err}")
            return None
        except requests.exceptions.Timeout as timeout_err:
            logger.error(f"Timeout Error สำหรับ {place_id}: {timeout_err}")
            return None
        except requests.exceptions.RequestException as req_err:
            logger.error(f"Request failed for {place_id}: {req_err}")
            return None
        except json.JSONDecodeError as json_err:
            logger.error(f"JSON decode error สำหรับ {place_id}: {json_err} - Response text: {response.text}")
            return None
        except Exception as e:
            logger.error(f"เกิดข้อผิดพลาดที่ไม่คาดคิดสำหรับ {place_id}: {e}")
            return None

    def bulk_get_places(self,
                        place_ids: List[str],
                        fields: str = None,
                        delay: float = 0.1,
                        save_to_file: str = None,
                        language: str = 'th') -> List[Dict]:
        """
        ดึงข้อมูลสถานที่หลายๆ แห่งพร้อมกัน

        Args:
            place_ids (List[str]): รายการ Google Place IDs
            fields (str): ฟิลด์ที่ต้องการ. หากไม่ระบุ จะใช้ฟิลด์เริ่มต้นของ get_place_details
            delay (float): หน่วงเวลาระหว่างการเรียก API (วินาที) เพื่อป้องกันการเกิน rate limit
            save_to_file (str): ชื่อไฟล์สำหรับบันทึกผลลัพธ์ (รองรับ .json และ .csv)
            language (str): ภาษาที่ต้องการให้ผลลัพธ์แสดง

        Returns:
            List[Dict]: รายการข้อมูลสถานที่ (รวมถึงรายการที่ดึงไม่สำเร็จพร้อมข้อความ error)
        """

        results = []
        total = len(place_ids)

        logger.info(f"เริ่มดึงข้อมูล {total} สถานที่...")

        for i, place_id in enumerate(place_ids, 1):
            logger.info(f"กำลังดึงข้อมูล {i}/{total}: {place_id}")

            place_data = self.get_place_details(place_id, fields, language)

            if place_data:
                # เพิ่ม place_id เข้าไปในข้อมูลเพื่อความสะดวกในการจัดการ
                place_data['place_id'] = place_id
                results.append(place_data)
                logger.info(f"✓ สำเร็จ: {place_data.get('name', 'ไม่ทราบชื่อ')}")
            else:
                logger.warning(f"✗ ไม่สามารถดึงข้อมูลได้: {place_id} (อาจเป็น Place ID ไม่ถูกต้อง/ไม่มีผลลัพธ์/ข้อผิดพลาดภายใน)")
                # เก็บข้อมูลที่ล้มเหลวไว้ด้วย เพื่อให้รู้ว่า Place ID ไหนมีปัญหา
                results.append({
                    'place_id': place_id,
                    'error': 'Failed to fetch data or ZERO_RESULTS'
                })

            # หน่วงเวลาเพื่อไม่ให้เกิน rate limit และปฏิบัติตามข้อกำหนดของ Google
            if delay > 0 and i < total:
                time.sleep(delay)

        successful_count = len([r for r in results if 'error' not in r])
        logger.info(f"เสร็จสิ้น! ดึงข้อมูลได้ {successful_count} จาก {total} สถานที่")

        # บันทึกลงไฟล์ถ้าระบุ
        if save_to_file:
            self.save_results(results, save_to_file)

        return results

    def save_results(self, results: List[Dict], filename: str):
        """บันทึกผลลัพธ์ลงไฟล์ (JSON หรือ CSV)"""

        if filename.endswith('.json'):
            try:
                with open(filename, 'w', encoding='utf-8') as f:
                    json.dump(results, f, ensure_ascii=False, indent=2)
                logger.info(f"บันทึกข้อมูล {len(results)} รายการลง {filename} แล้ว")
            except IOError as e:
                logger.error(f"ไม่สามารถบันทึกไฟล์ JSON ได้: {e}")

        elif filename.endswith('.csv'):
            try:
                df = self.results_to_dataframe(results)
                df.to_csv(filename, index=False, encoding='utf-8-sig')
                logger.info(f"บันทึกข้อมูล {len(df)} รายการลง {filename} แล้ว")
            except Exception as e:
                logger.error(f"ไม่สามารถบันทึกไฟล์ CSV ได้: {e}")
        else:
            logger.warning(f"รองรับเฉพาะไฟล์ .json และ .csv เท่านั้น ไม่สามารถบันทึกไฟล์: {filename}")

    def results_to_dataframe(self, results: List[Dict]) -> pd.DataFrame:
        """
        แปลงผลลัพธ์ดิบจาก API เป็น Pandas DataFrame ที่จัดระเบียบแล้ว

        Args:
            results (List[Dict]): รายการของข้อมูลสถานที่ที่ดึงมาจาก Google Places API

        Returns:
            pd.DataFrame: DataFrame ที่มีข้อมูลสถานที่ในรูปแบบตาราง
        """

        processed_data = []

        for result in results:
            if 'error' in result:
                # จัดการข้อมูลที่ error หรือดึงไม่สำเร็จ
                processed_data.append({
                    'place_id': result.get('place_id'),
                    'name': None,
                    'address': None,
                    'lat': None,
                    'lng': None,
                    'phone': None,
                    'website': None,
                    'rating': None,
                    'total_ratings': None,
                    'price_level': None,
                    'types': None,
                    'business_status': None,
                    'is_open_now': None,
                    'opening_hours_text': None, # เพิ่มฟิลด์สำหรับเวลาเปิด-ปิด
                    'photo_reference': None,    # เพิ่มฟิลด์สำหรับ photo reference
                    'error': result['error']
                })
                continue

            # ดึงพิกัด
            geometry = result.get('geometry', {})
            location = geometry.get('location', {})

            # ดึง types (แปลงเป็น string ที่คั่นด้วย comma)
            types = result.get('types', [])
            types_str = ', '.join(types) if types else None

            # ดึงเวลาเปิด-ปิด
            opening_hours = result.get('opening_hours', {})
            is_open_now = opening_hours.get('open_now', None)
            # ดึง weekday_text ซึ่งเป็นเวลาเปิด-ปิดแต่ละวันในรูปแบบข้อความ
            opening_hours_text = '\n'.join(opening_hours.get('weekday_text', [])) if opening_hours.get('weekday_text') else None

            # ดึง photo_reference จากรูปภาพแรก (ถ้ามี)
            photos = result.get('photos', [])
            photo_reference = photos[0].get('photo_reference') if photos else None

            processed_data.append({
                'place_id': result.get('place_id'),
                'name': result.get('name'),
                'address': result.get('formatted_address'),
                'lat': location.get('lat'),
                'lng': location.get('lng'),
                'phone': result.get('formatted_phone_number'),
                'website': result.get('website'),
                'rating': result.get('rating'),
                'total_ratings': result.get('user_ratings_total'),
                'price_level': result.get('price_level'),
                'types': types_str,
                'business_status': result.get('business_status'),
                'is_open_now': is_open_now,
                'opening_hours_text': opening_hours_text,
                'photo_reference': photo_reference,
                'error': None # ไม่มี error สำหรับรายการนี้
            })

        return pd.DataFrame(processed_data)

